# Python Program to
# demonstrate String slicing

# Creating a String
String1 = "GeekyCodes"
print("Initial String: ")
print(String1)

# Printing 3rd to 12th character
print("\nSlicing characters from 3-12: ")
print(String1[3:12])

# Printing characters between
# 3rd and 2nd last character
print("\nSlicing characters between " +
	"3rd and 2nd last character: ")
print(String1[3:-2])
